#include "ft_printf.h"

int		flags(char c)
{
	if (c == '#' || c == '0' || c == '-' || c == '+' || c == ' ' || c == '*')
		return (1);
	return (0);
}

int		length(char c)
{
	if (c == 'h' || c == 'l' || c == 'j' || c == 'z')
		return (1);
	return (0);
}

int		specifier(char c)
{
	if (c == 's' || c == 'S' || c == 'p' || c == 'd' || c == 'D' || c == 'i'
		|| c == 'o' || c == 'O' || c == 'u' || c == 'U' || c == 'x' || c == 'X'
		|| c == 'c' || c == 'C')
		return (1);
	return (0);
}
